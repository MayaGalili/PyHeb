def מספר_אקראי(מינימום, מקסימום):
    """מחזיר מספר אקראי בין מינימום למקסימום"""
    import random
    return random.randint(מינימום, מקסימום)

def עגל(מספר, ספרות_אחרי_הנקודה=0):
    """מעגל מספר לספרות אחרי הנקודה"""
    return round(מספר, ספרות_אחרי_הנקודה)

