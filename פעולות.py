# פונקציות מחרוזות
def אורך(טקסט):
    """מחזיר את אורך המחרוזת"""
    return len(טקסט)

def הפוך(טקסט):
    """הופך את סדר התווים במחרוזת"""
    return טקסט[::-1]

def חיבור_מחרוזות(מחרוזת1, מחרוזת2):
    """מחבר שתי מחרוזות"""
    return מחרוזת1 + מחרוזת2

def חתוך(טקסט, התחלה, סוף):
    """חותך חלק מהמחרוזת"""
    return טקסט[התחלה:סוף]

def מצא(טקסט, מה_לחפש):
    """מחפש מחרוזת בתוך מחרוזת אחרת"""
    return מה_לחפש in טקסט


# פונקציות רשימות
def רשימה_ריקה():
    """יוצר רשימה ריקה"""
    return []

def הוסף_לרשימה(רשימה, ערך):
    """מוסיף ערך לרשימה"""
    רשימה.append(ערך)

def הסר_מרשימה(רשימה, ערך):
    """מסיר ערך מהרשימה"""
    if ערך in רשימה:
        רשימה.remove(ערך)
    else:
        הדפס("הערך לא נמצא ברשימה")

def אורך_רשימה(רשימה):
    """מחזיר את אורך הרשימה"""
    return len(רשימה)

def רשימה_ממחרוזת(טקסט, מפריד=" "):
    """יוצר רשימה ממחרוזת"""
    return טקסט.split(מפריד)

def מחרוזת_מרשימה(רשימה, מפריד=" "):
    """יוצר מחרוזת מרשימה"""
    return מפריד.join(רשימה)


