# פונקציות מתמטיקה בסיסיות
def חיבור(א, ב):
    """מחזיר את סכום שני המספרים"""
    return א + ב

def חיסור(א, ב):
    """מחזיר את הפרש שני המספרים"""
    return א - ב

def כפל(א, ב):
    """מחזיר את מכפלת שני המספרים"""
    return א * ב

def חילוק(א, ב):
    """מחזיר את חלוקת שני המספרים"""
    if ב == 0:
        הדפס("שגיאה: לא ניתן לחלק באפס!")
        return 0
    return א / ב

def חזקה(א, ב):
    """מחזיר את א בחזקת ב"""
    return א ** ב

def שורש(א):
    """מחזיר את השורש הריבועי של א"""
    if א < 0:
        הדפס("שגיאה: לא ניתן לחשב שורש של מספר שלילי!")
        return 0
    return א ** 0.5


