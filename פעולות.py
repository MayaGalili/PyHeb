def הדפס(ערך):
    """הדפסה נכונה של ערכים בטרמינל עם תמיכה בעברית"""
    if isinstance(ערך, str) and any('\u0590' <= char <= '\u05FF' for char in ערך):
        # אם הטקסט מכיל תווים עבריים, השתמש בהדפסה מימין לשמאל
        print(f"\u202E{ערך}\u202C")
    else:
        print(ערך)


